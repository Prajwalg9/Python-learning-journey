#simple intrest = SI = (P * R * T)/100
#Where,
#P=Principal Amount
#R=Rate of Intrest
#T=Time duration

print("lets calculate Simple Intrest\n")
P=float(input("Enter Principal Amount: "))
R=float(input("Enter Rate of Intrest: "))
T=float(input("Enter Time duration in Years: "))

SI=(P*R*T)/100
print(f"\nSimple Intrest: {SI}")